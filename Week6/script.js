function searchActivities() {
    const city = document.getElementById('city').value;
    const activityType = document.getElementById('activity-type').value;
    const resultsContainer = document.getElementById('results');

    resultsContainer.innerHTML = '';

    const filteredActivities = activities.filter(activity => activity.city === city && activity.type === activityType);

    if (filteredActivities.length === 0) {
        resultsContainer.innerHTML = '<p>No activity found.</p>';
    } else {
        filteredActivities.forEach(activity => {
            const resultItem = document.createElement('div');
            resultItem.className = 'result-item';

            const img = document.createElement('img');
            img.src = activity.image;
            img.alt = activity.name;

            const description = document.createElement('div');
            description.innerHTML = `<h3>${activity.name}</h3><p>${activity.description}</p>`;

            resultItem.appendChild(img);
            resultItem.appendChild(description);
            resultsContainer.appendChild(resultItem);
        });
    }
}
